Exemplo #1 Exemplo mostrando a tag de fechamento incluindo uma nova linha final
<?php echo "Algum texto"; ?>
    Sem nova linha
<?= "Mas uma nova linha agora" ?>

O exemplo acima irá imprimir:
    Algum textoSem nova linha
    Mas uma nova linha agora


